/ec or /elvui - Открывает окно конфигурации
/bgstats - Показывает специфичные для поля битвы данные, если вы находитесь на поле битвы и закрыли их.
/hellokitty - Хотите розовый кавайный интерфейс? Мы выручим вас!
/harlemshake - Хочется тряски? Так сделайте это!
